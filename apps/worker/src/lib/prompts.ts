/**
 * ユーザー入力をXMLマーカーで区切るヘルパー
 * プロンプトインジェクション防御: ユーザー入力とシステム指示を明確に分離する
 */
export function wrapUserInput(label: string, content: string): string {
  return `<${label}>${content}</${label}>`
}

